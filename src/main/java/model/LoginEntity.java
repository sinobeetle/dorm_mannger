package model;

public class LoginEntity {
    String name=null;
    String pwd=null;

    public void setName(String name)
    {
        this.name=name;
    }
    public void setPwd(String pwd)
    {
        this.pwd=pwd;
    }
    public String getName()
    {
        return name;
    }
    public String getPwd()
    {
        return pwd;
    }

}
